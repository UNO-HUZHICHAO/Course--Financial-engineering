"""
LightGBM 简介:
    LightGBM 是微软开源的梯度提升决策树框架。

    主要优势:
    1. 训练速度快：使用 Leaf-wise 生长策略，效率高
    2. 内存占用低：优化的数据结构和算法
    3. 支持类别特征：可以直接处理类别变量，无需编码
    4. 准确率高：在多个机器学习竞赛中表现优异

    核心算法原理:
    - 基于 Gradient Boosting Decision Tree (GBDT)
    - 使用 Leaf-wise 生长策略（而非 Level-wise）
    - 支持 Histogram-based 算法加速
    - 支持 Gradient-based One-Side Sampling (GOSS)
    - 支持 Exclusive Feature Bundling (EFB)
"""

LGBM_PARAMS = {
    # ========== 目标函数和评估指标 ==========

    # objective: 目标函数类型
    # "binary" 表示二分类任务（预测是否违约）
    # 其他选项："regression"（回归）、"multiclass"（多分类）
    "objective": "binary",

    # metric: 评估指标
    # "auc" 表示使用 AUC（Area Under ROC Curve）评估
    # AUC 解释：
    #   - AUC = 0.5：模型预测能力等于随机猜测
    #   - AUC > 0.7：模型有一定预测能力
    #   - AUC > 0.8：模型预测能力较强
    #   - AUC > 0.9：模型预测能力很强（注意过拟合）
    "metric": "auc",

    # ========== 树的数量和学习率 ==========

    # n_estimators: 树的数量（迭代次数）
    # 500 表示最多训练 500 棵决策树
    # 配合 early_stopping 使用，实际训练可能少于这个数
    # 树越多，模型越复杂，但可能过拟合
    "n_estimators": 500,

    # learning_rate: 学习率（步长）
    # 控制每棵树对最终预测的贡献程度
    # 较小的学习率需要更多的树，但通常精度更高
    # 学习率小 + 树数量多 = 模型更精细但训练更慢
    # 学习率大 + 树数量少 = 模型更粗糙但训练更快
    "learning_rate": 0.05,

    # ========== 树的结构参数 ==========

    # max_depth: 树的最大深度
    # 控制模型复杂度，防止过拟合
    # 深度越大，模型越复杂，越容易记住训练数据（过拟合）
    # 深度越小，模型越简单，泛化能力可能更好
    # 通常范围：3-10，本项目使用 6
    "max_depth": 6,

    # num_leaves: 叶子节点数量
    # LightGBM 使用 Leaf-wise 生长策略
    # 这是控制模型复杂度的主要参数
    # 最大叶子数 = 2^max_depth，本例 max_depth=6，理论上最多 64 个叶子
    # 设置为 31 比理论最大值小，防止过拟合
    "num_leaves": 31,

    # min_child_samples: 叶子节点最小样本数
    # 一个叶子节点必须包含至少这么多样本
    # 防止树过于精细，叶子节点样本太少会过拟合
    # 增大这个值可以提高模型稳定性
    "min_child_samples": 20,

    # ========== 随机性和正则化参数 ==========

    # subsample: 数据采样比例（bagging）
    # 每棵树使用多少比例的数据训练
    # 0.8 表示每棵树随机选择 80% 的数据
    # 增加随机性，防止过拟合，提高泛化能力
    # 类似于 Random Forest 的 bootstrap 采样
    "subsample": 0.8,

    # colsample_bytree: 特征采样比例
    # 每棵树使用多少比例的特征
    # 0.8 表示每棵树随机选择 80% 的特征
    # 增加模型多样性，防止过拟合
    # 类似于 Random Forest 的随机特征选择
    "colsample_bytree": 0.8,

    # reg_alpha: L1 正则化参数
    # 使部分特征权重为零，达到特征选择的效果
    # L1 正则化会使模型更稀疏
    # 适合特征数量多、希望筛选重要特征的场景
    "reg_alpha": 0.1,

    # reg_lambda: L2 正则化参数
    # 权重衰减，防止权重过大
    # L2 正则化会使所有特征权重都减小
    # 适合防止模型过于依赖某些特征
    "reg_lambda": 1.0,

    # ========== 其他参数 ==========

    # random_state: 随机种子
    # 42 是常用的随机种子值
    # 确保每次运行结果一致，便于实验对比
    # 如果设为 None，每次运行结果不同
    "random_state": 42,

    # verbose: 日志输出级别
    # -1 表示不输出训练过程日志
    # 如果想看训练过程，可以设置为正数（如 100 表示每100轮输出）
    "verbose": -1,

    # n_jobs: 并行计算线程数
    # -1 表示使用所有可用的 CPU 核心
    # 可以加快训练速度
    "n_jobs": -1,
}


