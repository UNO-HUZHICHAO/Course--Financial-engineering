# CatBoost 模型参数配置
# ==============================================================================

"""
CatBoost 简介:
    CatBoost 是 Yandex（俄罗斯搜索引擎）开源的梯度提升树框架。

    主要优势:
    1. 类别特征处理出色：内置 Ordered Target Statistics 编码
    2. Ordered Boosting：减少过拟合的特殊提升策略
    3. 支持 GPU 加速：充分利用 GPU 资源
    4. 易于使用：参数少，默认配置效果好

    核心算法原理:
    - Ordered Target Statistics：处理类别特征的特殊编码方法
    - Ordered Boosting：避免目标泄漏的特殊提升策略
    - Symmetric Trees：使用对称树结构，提高预测速度

    与 LightGBM 的区别:
    - LightGBM 聚焦速度和效率
    - CatBoost 聚焦类别特征和防止过拟合
"""

CATBOOST_PARAMS = {
    # ========== 迭代参数 ==========

    # iterations: 迭代次数（树的数量）
    # 与 LightGBM 的 n_estimators 对应
    # 500 表示最多训练 500 棵树
    "iterations": 500,

    # learning_rate: 学习率
    # 与 LightGBM 保持一致，便于对比
    # CatBoost 对学习率敏感，通常使用较小的值
    "learning_rate": 0.05,

    # ========== 树的结构参数 ==========

    # depth: 树的深度
    # 与 LightGBM 的 max_depth 对应
    # CatBoost 推荐值范围：1-16，常用 4-10
    # CatBoost 使用对称树，深度影响树结构
    "depth": 6,

    # l2_leaf_reg: L2 正则化参数
    # 防止叶子节点权重过大
    # 增大可以防止过拟合，但可能降低精度
    # CatBoost 推荐值：1-10
    "l2_leaf_reg": 3.0,

    # ========== 评估和训练控制 ==========

    # eval_metric: 评估指标
    # "AUC" 使用 AUC 评估，与 LightGBM 一致
    # 其他选项："Accuracy"、"Logloss"、"F1" 等
    "eval_metric": "AUC",

    # early_stopping_rounds: 早停轮数
    # 连续这么多轮 AUC 无提升，则停止训练
    # 50 表示连续 50 轮无改善则停止
    # 防止过拟合，节省训练时间
    "early_stopping_rounds": 50,

    # ========== 其他参数 ==========

    # random_seed: 随机种子
    # 与 LightGBM 的 random_state 对应
    # 确保结果可复现
    "random_seed": 42,

    # verbose: 日志输出间隔
    # 0 表示不输出训练日志
    # 正数表示每隔多少轮输出一次
    "verbose": 0,

    # use_best_model: 是否使用最佳模型
    # True 表示返回验证集上表现最好的模型
    # False 表示返回最后训练完成的模型
    "use_best_model": True,

    # allow_writing_files: 是否允许写文件
    # False 表示不写临时文件，节省磁盘空间
    "allow_writing_files": False,
}


